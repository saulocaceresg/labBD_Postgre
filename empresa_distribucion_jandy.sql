create TABLE CARGO(
	CarId INTEGER PRIMARY KEY CHECK (CarId BETWEEN 1 AND 99),
	CarDes VARCHAR(40) NOT NULL,
	CarSld NUMERIC(5, 2) CHECK (CarSld >= 1) NOT NULL,
	CarEstReg BOOLEAN NOT NULL
);
create TABLE DETALLE(
	DetId INTEGER PRIMARY KEY CHECK (DetId BETWEEN 1 AND 99999999),
	CliId INTEGER NOT NULL,
	FOREIGN KEY (CliId) REFERENCES CLIENTES(CliId),
	DetProDes VARCHAR(100),
	DetCan INTEGER CHECK (DetCan BETWEEN 1 AND 100) NOT NULL,
	DetEstReg BOOLEAN NOT NULL
);

create TABLE REGION(
	RegId SMALLINT PRIMARY KEY CHECK (RegId BETWEEN 1 AND 26),
	RegNom VARCHAR(20) NOT NULL,
	RegDes VARCHAR(40),
	RegEstReg BOOLEAN NOT NULL
);

create TABLE CIUDAD(
	CiuId INTEGER PRIMARY KEY CHECK (CiuId BETWEEN 1 AND 9999),
	RegId SMALLINT NOT NULL,
	FOREIGN KEY (RegId) REFERENCES REGION(RegId),
	CiuNom VARCHAR(20) NOT NULL,
	CiuDes VARCHAR(40),
	CiuEstReg BOOLEAN NOT NULL
);

create TABLE ROL_USUARIO(
	RolId INTEGER PRIMARY KEY CHECK (RolId BETWEEN 1 AND 99),
	RolNom VARCHAR(20) NOT NULL,
	RolDes VARCHAR(50),
	RolEstReg BOOLEAN NOT NULL
);

create TABLE CREDITOS(
	CreId INTEGER PRIMARY KEY CHECK (CreId BETWEEN 1 AND 999999),
	CreDes VARCHAR(40),
	CreEstReg BOOLEAN NOT NULL
);

create TABLE UNIDAD_MEDIDA(
	UniId VARCHAR(3) PRIMARY KEY,
	UniNom VARCHAR(15) NOT NULL,
	UniEstReg BOOLEAN NOT NULL
);

create TABLE CATEGORIA(
	CatId INTEGER PRIMARY KEY CHECK (CatId BETWEEN 1 AND 999999),
	CatNom VARCHAR(50) NOT NULL,
	CatEstReg BOOLEAN NOT NULL
);

create TABLE CLIENTE(
	CliId INTEGER PRIMARY KEY CHECK (CliId BETWEEN 1 AND 999999),
	CliNom VARCHAR(50) NOT NULL,
	CliApePat VARCHAR(50) NOT NULL,
	CliApeMat VARCHAR(50) NOT NULL,
	CliCorreo VARCHAR(100) NOT NULL,
	CliDir VARCHAR(100) NOT NULL,
	CliLimCred INTEGER CHECK (CliLimCred > 0) NOT NULL,
	RepId INTEGER NOT NULL
	FOREIGN KEY (RepId) REFERENCES REP_VENTA(RepId),
	CliEstReg BOOLEAN NOT NULL
);

create TABLE REP_VENTA(
	RepId INTEGER PRIMARY KEY CHECK (RepId BETWEEN 1 AND 999999),
	RepNom VARCHAR(50) NOT NULL,
	RepEd INTEGER CHECK (RepEd BETWEEN 18 AND 100),
	EmpNumOfi INTEGER NOT NULL,
	FOREIGN KEY (EmpNumOfi) REFERENCES OFICINA(EmpNumOfi),
	RepCar VARCHAR(50) NOT NULL,
	RepCon INTEGER CHECK (RepCon BETWEEN 1 AND 99999999) NOT NULL,
	RepCuo INTEGER CHECK (RepCuo BETWEEN 1 AND 9999999999) NOT NULL,
	RepVenReal INTEGER CHECK (RepVenReal BETWEEN 1 AND 9999999999) NOT NULL,
	RepEstReg BOOLEAN NOT NULL
);

create TABLE FACTURA(
	FacId INTEGER PRIMARY KEY CHECK (FacId BETWEEN 1 AND 99999999),
	CliId INTEGER NOT NULL,
	FOREIGN KEY (CliId) REFERENCES CLIENTE(CliId),
	RepId INTEGER NOT NULL, 
	FOREIGN KEY (RepId) REFERENCES REP_VENTA(RepId),
	FabId VARCHAR NOT NULL,
	FOREIGN KEY (FabId) REFERENCES FABRICANTE(FabId),
	FacProDes VARCHAR(100),
	CanProId INTEGER CHECK (CanProId BETWEEN 1 AND 999) NOT NULL,
	FacImp INTEGER CHECK (FacImp > 0) NOT NULL,
	FacAño INTEGER CHECK (FacAño > 2007) NOT NULL,
	FacMes INTEGER CHECK (FacMes BETWEEN 1 AND 12) NOT NULL,
	FacDia INTEGER CHECK (FacDia BETWEEN 1 AND 31) NOT NULL,
	FacEstReg BOOLEAN NOT NULL
);

create TABLE CANT_PRODUCTO_PEDIDO(
	CanProdId INTEGER PRIMARY KEY CHECK (CanProdId BETWEEN 1 AND 999999),
	ProId INTEGER NOT NULL,
	FOREIGN KEY (ProId) REFERENCES PRODUCTO(ProId),
	CanEstReg BOOLEAN NOT NULL
); 

create TABLE PRODUCTO(
	ProId INTEGER PRIMARY KEY CHECK (ProId BETWEEN 1 AND 999999),
	FabId VARCHAR NOT NULL,
	FOREIGN KEY (FabId) REFERENCES FABRICANTE(FabId),
	ProDes VARCHAR(100) NOT NULL,
	ProPre DECIMAL(10, 2) CHECK (ProPre > 0) NOT NULL,
	ProSto INTEGER CHECK (ProSto >= 0) NOT NULL,
	CatId INTEGER NOT NULL,
	FOREIGN KEY (CatId) REFERENCES CATEGORIA(CatId),
	ProUniMed VARCHAR(5) NOT NULL,
	FOREIGN KEY (ProUniMed) REFERENCES UNIDAD_MEDIDA(UniId),
	ProEstReg BOOLEAN NOT NULL
);

create TABLE FABRICANTE(
	FabId INTEGER PRIMARY KEY CHECK (FabId BETWEEN 1 AND 99),
	FabNom VARCHAR(20) NOT NULL,
	TipoId INTEGER CHECK (TipoId BETWEEN 1 AND 999999) NOT NULL,
	FabEstReg BOOLEAN NOT NULL
);