create TABLE CARGO(
	CarId INTEGER PRIMARY KEY CHECK (CarId BETWEEN 1 AND 99),
	CarDes VARCHAR(40) NOT NULL,
	CarSld NUMERIC(5, 2) CHECK (CarSld >= 1) NOT NULL,
	CarEstReg BOOLEAN NOT NULL
);
create TABLE DETALLE(
	DetId INTEGER PRIMARY KEY CHECK (DetId BETWEEN 1 AND 99999999),
	CliId INTEGER NOT NULL,
	FOREIGN KEY (CliId) REFERENCES CLIENTES(CliId),
	DetProDes VARCHAR(100),
	DetCan INTEGER CHECK (DetCan BETWEEN 1 AND 100) NOT NULL,
	DetEstReg BOOLEAN NOT NULL
);

create TABLE REGION(
	RegId SMALLINT PRIMARY KEY CHECK (RegId BETWEEN 1 AND 26),
	RegNom VARCHAR(20) NOT NULL,
	RegDes VARCHAR(40),
	RegEstReg BOOLEAN NOT NULL
);

create TABLE CIUDAD(
	CiuId INTEGER PRIMARY KEY CHECK (CiuId BETWEEN 1 AND 9999),
	RegId SMALLINT NOT NULL,
	FOREIGN KEY (RegId) REFERENCES REGION(RegId),
	CiuNom VARCHAR(20) NOT NULL,
	CiuDes VARCHAR(40),
	CiuEstReg BOOLEAN NOT NULL
);

create TABLE ROL_USUARIO(
	RolId INTEGER PRIMARY KEY CHECK (RolId BETWEEN 1 AND 99),
	RolNom VARCHAR(20) NOT NULL,
	RolDes VARCHAR(50),
	RolEstReg BOOLEAN NOT NULL
);

create TABLE CREDITOS(
	CreId INTEGER PRIMARY KEY CHECK (CreId BETWEEN 1 AND 999999),
	CreDes VARCHAR(40),
	CreEstReg BOOLEAN NOT NULL
);

create TABLE UNIDAD_MEDIDA(
	UniId VARCHAR(3) PRIMARY KEY,
	UniNom VARCHAR(15) NOT NULL,
	UniEstReg BOOLEAN NOT NULL
);

create TABLE CATEGORIA(
	CatId INTEGER PRIMARY KEY CHECK (CatId BETWEEN 1 AND 999999),
	CatNom VARCHAR(50) NOT NULL,
	CatEstReg BOOLEAN NOT NULL
);